          <br/> （2）发起追号任务订单，将一次性扣除追号所需金额，账户余额不满足时，任务将不予 发起。
          <br/> （3）当追号任务符合玩家设置的追号停止条件时，系统将自动终止追号任务，如还有剩 余未追期数，系统将剩余期数的追号金额自动退还至玩家购彩账户。
          <br/> （4）玩家可以随时取消任意一期未开奖的追号任务或者终止整个追号任务，未追期数的 追号金额自动退还至玩家的购彩账户。
          <br/> （5）高频游戏最大可追号期数为该彩种24小时内全部期数。如遇撤单，会跳过该期继续 追下一期直至符合玩家设置的追号停止条件或者追号完成。
