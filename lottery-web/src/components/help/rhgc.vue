          <br/>同倍追号：同倍追号，即设定追号期数和倍数之后，所有的期数的投注倍数都与设置倍数 相同。
          <br/> 翻倍追号：需要设置“隔期”、“翻倍”，点击“立即生成”，系统将立即生成为与设置一致的投 注。
          <br/> 利润率追号：指追号计划中保证中奖的最低利润，设定“最低利润率”、“起始倍数”、“追号 期数”，点击“立即生成”之后，系统会为您计算投注倍数。
        </p>
