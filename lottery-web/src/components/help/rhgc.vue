          <br/> 因官方未开奖撤单：如果官方机构延迟开奖，平台将顺延开奖时间，如果官方未开奖，平台将对本期所有投注做撤单处理，如果在撤单之后官方开奖，仍维持撤单处理。撤单后， 投注金额将如数返回到您的账户中。
        </p>
